<?php
// ============================================================
// db.php — ไฟล์เชื่อมต่อฐานข้อมูลกลาง (ใช้ซ้ำในทุกไฟล์)
// วิธีใช้: ไฟล์อื่นเรียก  require 'db.php';  แล้วใช้ตัวแปร $conn ได้เลย
// ============================================================

$host = 'localhost';      // เครื่องฐานข้อมูล (บนเครื่องตัวเองคือ localhost)
$user = 'root';           // ผู้ใช้เริ่มต้นของ XAMPP
$pass = '';               // XAMPP ปกติรหัสผ่าน root เป็นค่าว่าง
$dbname = 'atsoft_day2';  // ชื่อฐานข้อมูลที่เราสร้างไว้

// เชื่อมต่อ
$conn = mysqli_connect($host, $user, $pass, $dbname);

// ถ้าเชื่อมไม่ได้ ให้หยุดและบอก error
if (!$conn) {
    die('เชื่อมต่อฐานข้อมูลไม่สำเร็จ: ' . mysqli_connect_error());
}

// ตั้งค่าให้รองรับภาษาไทย (utf8mb4) ป้องกันตัวอักษรเพี้ยน
mysqli_set_charset($conn, 'utf8mb4');
